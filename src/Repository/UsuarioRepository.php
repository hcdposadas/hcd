	public function loadUserByUsername( $password ) {
		return $this->createQueryBuilder( 'u' )
		            ->where( 'u.password = :password ' )
		            ->setParameter( 'password', $password )
		            ->getQuery()
		            ->getOneOrNullResult();
	}
